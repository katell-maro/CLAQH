           				 <p>
              				Le Hg tend à s'accumuler dans les sédiments par précipitation/sorption/floculation puis sédimentation. Les sédiments constituent donc un réservoir de Hg dans lesquels certaines bactéries anaérobies, principalement les bactéries sulfato-réductrices, sont responsables de la production de méthylmercure. Une mise en suspension des sédiments par des phénomènes naturels (bioturbations, tempêtes) ou anthropiques va favoriser le passage de ce méthylmercure dans les chaînes alimentaires. Certaines données récentes font état de la présence de ces bactéries sulfato-réductrices au niveau des racines de plantes aquatiques dans la colonne d'eau ou elles présenteraient de fortes activités de méthylation, y compris au niveau des lacs Landais. Ces microenvironnements, riches en polymères organiques sont connus pour leur faculté à accumuler les éléments métalliques, dont le Hg inorganique. La présence de sulfato-réducteurs et leur activité de méthylation du Hg dans ces micro-écosystèmes (périphyton à la surface des racines) pourrait faciliter le transfert de ce métal vers les niveaux supérieurs de la chaîne trophique, par l'intermédiaire d'organismes périphytophages (qui consomment le périphyton et qui ne broutent pas les plantes).
            			</p>
            			<p>
              				Depuis 2009, nous conduisons une étude dans différents écosystèmes aquatiques de la zone côtière landaise (lac, courants, rivière et estuaire). Ces travaux ont pour objectif de réaliser un état des lieux de l’abondance en microorganismes sulfato-réducteurs (MSR), leur biodiversité et leur impact sur les transferts du Hg vers les réseaux trophiques supérieurs dont les poissons. Les prélèvements effectués lors de deux campagnes de terrain en Septembre 2009 et Septembre 2010 ont permis d’effectuer des analyses multiples. La caractérisation et la cartographie des MSR dans les sédiments, les racines de macrophytes aquatiques, et l’eau ont pu être réalisées. Les résultats obtenus ont permis de mettre en évidence le développement de MSR dans l’ensemble des compartiments étudiés, dont les racines de plantes aquatiques. Lors de la deuxième campagne terrain, des souches de sulfato-réducteurs ont été isolées en laboratoire du périphyton de plantes aquatiques puis identifiées. Certaines d’entre elles se sont révélées capables de méthyler le Hg à des taux variables. Récemment deux gènes, hgcA et hgcB, ont été identifiés comme intervenant dans la méthylation du Hg aussi bien chez les MSR, que chez d’autres types de microorganismes comme les bactéries ferri-réductrices et les archées méthanogènes.
